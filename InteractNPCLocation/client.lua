AddEventHandler('playerSpawned', function(resourceName)
    for _,location in ipairs(markets) do
        x = location[1]
        y = location[2]
        z = location[3]
        heading = location[4]

        model = location[5]

        anim = location[6]
        animbone = location[7]

        local Hash = GetHashKey(model)
        repeat 
            Wait(200)
            RequestModel(Hash)
        until HasModelLoaded(Hash)
        local Ped = CreatePed(1, Hash, x,y,z-0.95, 0.0, false, true)

        SetEntityHeading(Ped, heading)
        FreezeEntityPosition(Ped, true)
        loadAnimDict( anim )
        TaskPlayAnim(Ped, anim, animbone, 1.0,-1.0, -1, 1, 1, true, true, true)
    end
end)

function loadAnimDict( dict )
    while ( not HasAnimDictLoaded( dict ) ) do
        RequestAnimDict( dict )
        Citizen.Wait(5)
    end
end
